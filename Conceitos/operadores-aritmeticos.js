console.log("subtração")
console.log(`10 - 7 = ${10 - 7}`) // 3

console.log("multiplicação")
console.log(`2 * 2 = ${2 * 2}`) // 4

console.log("divisão")
console.log(`11 / 2 = ${11 / 2}`) // 5.5

console.log("resto")
console.log(`11 % 2 = ${11 % 2}`) // 1

console.log("exponencial")
console.log(`4 ** 4 = ${4 ** 4}`) //256

